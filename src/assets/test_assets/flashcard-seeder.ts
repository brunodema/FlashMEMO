// this code aims to make the flashcard data a bit more believable. Even though the content assignments won't make nay sense, they are correctly attached to the DeckId pool, and the due dates are calculated based on the LastUpdated date and flashcard levels.

export function flashcardSeeder(): typeof flashcardJson {
  let deckIdPool = deckJson.map((x) => x.deckId); // sets ID pool from Decks
  flashcardJson.map(
    (f) =>
      (f.deckId = deckIdPool[Math.floor(Math.random() * deckIdPool.length)])
  ); // assigns IDs
  flashcardJson.map((f) => {
    let currDate = new Date(f.lastUpdated);
    currDate.setDate(currDate.getDate() + 2 ** f.level);
    f.dueDate = currDate.toISOString();
  }); // calculates DueDates
  flashcardJson.map((f) => (f.dueDate = f.dueDate.replace('.000Z', ''))); // removes trailing chars

  const lorem = new LoremIpsum({
    sentencesPerParagraph: {
      max: 8,
      min: 4,
    },
    wordsPerSentence: {
      max: 15,
      min: 0,
    },
  });

  flashcardJson.map((f) =>
    Object.assign(f, {
      content4: lorem.generateSentences(1),
      content5: lorem.generateSentences(1),
      content6: lorem.generateSentences(1),
    })
  );

  flashcardJson.map((f) => {
    if (f.content4 === '.') f.content4 = '';
    if (f.content5 === '.') f.content5 = '';
    if (f.content6 === '.') f.content6 = '';
  });

  //console.log(flashcardJson.map(f => {return { lu: f.lastUpdated , dt: f.dueDate}})) // for debugging
  return flashcardJson;
}
